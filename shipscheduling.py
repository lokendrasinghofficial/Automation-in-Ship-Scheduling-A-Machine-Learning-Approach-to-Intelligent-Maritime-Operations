def schedule_ships(model, ship_data):
    """
    Schedules ships optimally based on predicted waiting times.

    Params:
    - model: Trained ML model (RandomForestRegressor)
    - ship_data: List of dictionaries with ship details

    Returns:
    - Optimized schedule (DataFrame)
    """

    # Convert input data to DataFrame
    df_ships = pd.DataFrame(ship_data)

    # Predict waiting time for each ship
    df_ships['Predicted_Waiting_Time'] = df_ships.apply(lambda x: model.predict(pd.DataFrame([x.drop('Ship_ID')]))[0], axis=1)

    # Sort ships based on Shortest Job First (SJF) scheduling
    df_ships = df_ships.sort_values(by=['Predicted_Waiting_Time'])

    # Assign docking times
    current_time = 0
    schedule = []

    for _, row in df_ships.iterrows():
        ship_id = row['Ship_ID']
        arrival_time = row['Arrival_Time']
        service_time = row['Service_Time']

        # Ensure ship is docked at or after its arrival time
        start_time = max(current_time, arrival_time)
        end_time = start_time + service_time

        # Store schedule
        schedule.append({
            'Ship_ID': ship_id,
            'Start_Time': start_time,
            'End_Time': end_time,
            'Predicted_Waiting_Time': row['Predicted_Waiting_Time']
        })

        # Update current time
        current_time = end_time

    # Convert to DataFrame
    df_schedule = pd.DataFrame(schedule)

    return df_schedule

