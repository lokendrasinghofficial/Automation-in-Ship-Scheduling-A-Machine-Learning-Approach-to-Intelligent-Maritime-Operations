# Objective function: Calculate total waiting time for a given ship order
def calculate_total_waiting_time(ship_order, ships):
    current_time = 0
    total_waiting_time = 0

    for ship_id in ship_order:
        ship = ships[ship_id]
        arrival_time = ship['Arrival_Time']
        service_time = ship['Service_Time']

        # Ship can only start after it arrives
        start_time = max(current_time, arrival_time)
        waiting_time = start_time - arrival_time

        # Add waiting time to total
        total_waiting_time += waiting_time
        current_time = start_time + service_time  # Update time after ship leaves

    return total_waiting_time

# Generate initial population (random ship orders)
def generate_population(ships, size=10):
    population = []
    ship_ids = list(ships.keys())

    for _ in range(size):
        random.shuffle(ship_ids)  # Shuffle ship order randomly
        population.append(ship_ids.copy())

    return population

# Selection: Choose the best schedules (lowest waiting time)
def select_best(population, ships, top_k=5):
    sorted_population = sorted(population, key=lambda order: calculate_total_waiting_time(order, ships))
    return sorted_population[:top_k]  # Return top best solutions

# Crossover: Mix two schedules to create a new one
def crossover(parent1, parent2):
    size = len(parent1)
    child = [-1] * size

    # Select a crossover point
    crossover_point = random.randint(1, size - 2)

    # Copy first part from parent1
    child[:crossover_point] = parent1[:crossover_point]

    # Fill remaining slots with ships from parent2 (in order)
    remaining_ships = [ship for ship in parent2 if ship not in child]
    child[crossover_point:] = remaining_ships

    return child

# Mutation: Swap two ships randomly
def mutate(schedule):
    idx1, idx2 = random.sample(range(len(schedule)), 2)
    schedule[idx1], schedule[idx2] = schedule[idx2], schedule[idx1]
    return schedule

# Genetic Algorithm to optimize ship scheduling
def genetic_algorithm(ships, generations=50, population_size=10):
    population = generate_population(ships, population_size)

    for _ in range(generations):
        best_schedules = select_best(population, ships)  # Select best
        new_population = best_schedules.copy()

        # Create new offspring
        while len(new_population) < population_size:
            parent1, parent2 = random.sample(best_schedules, 2)
            child = crossover(parent1, parent2)
            if random.random() < 0.2:  # Mutation chance 20%
                child = mutate(child)
            new_population.append(child)

        population = new_population  # Update population

    # Return the best schedule
    best_schedule = select_best(population, ships)[0]
    return best_schedule, calculate_total_waiting_time(best_schedule, ships)

